package ir.dr_hesaabi.infa.modol;

public class SearchProductFeature {

    private String name;
    private String newCost;
    private String oldCost;
    private String off;
    private String image;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNewCost() {
        return newCost;
    }

    public void setNewCost(String newCost) {
        this.newCost = newCost;
    }
    public String getOldCost() {
        return oldCost;
    }

    public void setOldCost(String oldCost) {
        this.oldCost = oldCost;
    }

    public String getOff() {
        return off;
    }

    public void setOff(String off) {
        this.off = off;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
